package com.montefiore.gaulthiergain.chatadhoc;

class MemberData {

    private String name;
    private String color;

    public MemberData() {
    }

    public MemberData(String name, String color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }
}
